void	display_rs(int	result)
{
	if (!result)
		printf("\e[31m✗\e[0m");
	if (result)
		printf("\e[32m✓\e[0m");
}
 
void	prnt_ko(int res[42])
{
	printf("\e[31m");
	for (int i = 0; i < 42; i++)
	{
		if (!res[i])
			printf("|%i", i + 1);
	}
	printf("|");
	printf("\e[0m");
}